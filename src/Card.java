public class Card {
    // PROPERTIES
    private String color;
    private char rank;

    // CONSTRUCTORS
    public Card() {}

    // ACCESSORS
    public String getColor() {
        return this.color;
    }

    public char getRank() {
        return this.rank;
    }

    // MUTATORS
    public void setColor(String color) {
        this.color = color;
    }

    public void setRank(char rank) {
        this.rank = rank;
    }

    // METHODS
}
